//js variable

//var variable
//let variable
//const variable

//var variable is globally scoped
var a=10;
var b=20;
console.log(a+b);

//let variable is block scoped
let c=30;
let d=40;
console.log(c+d);


//const variable is block scoped and cannot be reassigned
const e=50;
const f=60;
console.log(e+f);